package com.l1;

/**Class encapsulates two field to decsribe position on the plain of any object
 *
 */
public class Coordinates {
    private int x;
    private int y;

    /*Gets y coordinate value
     *
     * @return y coordinate
     */
    public int getY() {
        return y;
    }

    /**Sets y coordinate value
     *
     * @param y y coordinate
     */
    public void setY(int y) {
        this.y = y;
    }

    /**Gets x coordinate value
     *
     * @return x coordinate
     */
    public int getX() {
        return x;
    }

    /**Sets x coordinate value
     *
     * @param x x coordinate
     */
    public void setX(int x) {
        this.x = x;
    }
}
